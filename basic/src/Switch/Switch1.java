package Switch;

public class Switch1 {
	public static void main(String[] args) {
		
		char grade ='B';
		
		switch(grade) {
		
		case 'A' :
			System.out.println("Excellent");
			break;
			
		case 'B' :
			System.out.println("Good");
			break;
			
		case 'c' :
			System.out.println("poor");
			break;
			
		default :
			System.out.println("average");
		}
		
		
		
		
		
	}

}
